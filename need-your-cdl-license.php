<h1 style="color:red;">Which Type of CDL License Do I Need?</h1>

<p>Most states offer both Class A CDL licenses and Class B CDL licenses.</p>

<p>A <b>Class A CDL License</b> is needed to operate any combination of vehicles with a <i>gross vehicle weight rating</i> (<b>GVWR</b>) of 26,001 or more pounds, provided the GVWR of the vehicle(s) being towed is in excess of 10,000 pounds. Vehicles requiring a Class A CDL license are primarily tractor-trailers.</p>
<p>A <b>Class B CDL License</b> is needed to operate any single vehicle with a GVWR of 26,001 or more pounds, or any such vehicle towing a vehicle with a GVWR not in excess of 10,000 pounds. Vehicles requiring a Class B CDL license may include buses (regardless of size), passenger vans, motorhomes, standard-sized dump trucks, tow trucks, cement mixers, delivery vans, garbage trucks, utility vehicles and any single truck with a GVWR of 26,001 pounds or more, cars and cars with trailers.</p>

